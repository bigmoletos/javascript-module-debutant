function chiffreRomain(nombre) {
 
    if (nombre === 1) {
    return I;
  }
  if (nombre === 2) {
    return II;
  }
  if (nombre === 3) {
    return III;
  }
  if (nombre === 5) {
    return V;
  }
  if (nombre === 10) {
    return X;
  }
  if (nombre === 50) {
    return L;
  }
  if (nombre === 100) {
    return C;
  }
  if (nombre === 500) {
    return D;
  }
  if (nombre === 1000) {
    return M;
  }
}
